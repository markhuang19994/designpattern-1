package orca.try3;

public class Data {

    long timestamp;

    double temp;
    double vibx;
    double viby;
    double vibz;

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    public double getTemp() {
        return temp;
    }

    public void setTemp(double temp) {
        this.temp = temp;
    }

    public double getVibx() {
        return vibx;
    }

    public void setVibx(double vibx) {
        this.vibx = vibx;
    }

    public double getViby() {
        return viby;
    }

    public void setViby(double viby) {
        this.viby = viby;
    }

    public double getVibz() {
        return vibz;
    }

    public void setVibz(double vibz) {
        this.vibz = vibz;
    }
}
